public class Partita {

    private String squadraCasa;
    private String squadraOspite;
    private int punteggioCasa;
    private int punteggioOspiti;
    private int setVintiCasa;
    private int setVintiOspiti;
    private String data;

    public Partita() {
    }

    public String getSquadraCasa() {
        return squadraCasa;
    }

    public void setSquadraCasa(String squadraCasa) {
        this.squadraCasa = squadraCasa;
    }

    public String getSquadraOspite() {
        return squadraOspite;
    }

    public void setSquadraOspite(String squadraOspite) {
        this.squadraOspite = squadraOspite;
    }

    public int getPunteggioCasa() {
        return punteggioCasa;
    }

    public void setPunteggioCasa(int punteggioCasa) {
        this.punteggioCasa = punteggioCasa;
    }

    public int getPunteggioOspiti() {
        return punteggioOspiti;
    }

    public void setPunteggioOspiti(int punteggioOspiti) {
        this.punteggioOspiti = punteggioOspiti;
    }

    public int getSetVintiCasa() {
        return setVintiCasa;
    }

    public void setSetVintiCasa(int setVintiCasa) {
        this.setVintiCasa = setVintiCasa;
    }

    public int getSetVintiOspiti() {
        return setVintiOspiti;
    }

    public void setSetVintiOspiti(int setVintiOspiti) {
        this.setVintiOspiti = setVintiOspiti;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}